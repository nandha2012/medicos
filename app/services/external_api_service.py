def get_log_data_from_api():
    data = {
        'token': token,
        'content': 'log',
        'logtype': 'record',
        'user': '',
        'record': '',
        'beginTime': '2025-06-19 01:00',
        'endTime': get_current_time_str(),
        'format': 'json',
        'returnFormat': 'json'
    }
    try:
        response = requests.post(end_point,data=data)
        response.raise_for_status()
        return response.json()
    except Exception as e:
        print(f"‚ùå Error getting log data from API: {e}")
        return None

def get_log_detail_data_from_api(records, batch_size=5):
    details = []
     # Process records in batches
    for i in range(0, len(records), batch_size):
        batch = records[i:i + batch_size]
        print(f"üì¶ Processing batch {i//batch_size + 1}: records {i+1}-{min(i+batch_size, len(records))}")
        data = get_batch_record_data_from_api(batch)
        # data = generate_fake_detail_record(batch,1)
        print(f'{len(data)} found for {batch}')
        if data is None:
            print(f"‚ùå Error getting single record data from API")
            continue
        for item in data:
            details.append(item)
    return details


